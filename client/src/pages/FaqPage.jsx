function FaqItem({ q, a }) {
  const [open, setOpen] = useState(false);
  return (
    <div className="border-b border-gray-100 dark:border-gray-700 last:border-0">
      <button
        onClick={() => setOpen(v => !v)}
        className="w-full flex items-center justify-between py-4 text-left gap-4 group"
      >
        <span className="font-medium text-gray-900 dark:text-white group-hover:text-blue-600 dark:group-hover:text-blue-400 transition">
          {q}
        </span>
        {open
          ? <ChevronUp className="w-5 h-5 text-blue-500 shrink-0" />
          : <ChevronDown className="w-5 h-5 text-gray-400 shrink-0" />
        }
      </button>
      {open && (
        <div className="pb-4 text-gray-600 dark:text-gray-400 text-sm leading-relaxed">
          {a}
        </div>
      )}
    </div>
  );
}

